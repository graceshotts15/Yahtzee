function Roll1() {
    var rollResult = Math.floor(Math.random() * 6) + 1;
    var pic = document.getElementById("1");
     if (rollResult == 1) pic.setAttribute('src', '1.png');
    else if (rollResult == 2) pic.setAttribute('src', '2.png');
    else if (rollResult == 3) pic.setAttribute('src', '3.png');
    else if (rollResult == 4) pic.setAttribute('src', '4.png');
    else if (rollResult == 5) pic.setAttribute('src', '5.png');
    else  pic.setAttribute('src', '6.png');
}
